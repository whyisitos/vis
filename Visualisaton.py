import streamlit as st
import streamlit.components.v1 as components

# 1. Налаштування сторінки
st.set_page_config(page_title="Міграційні тенденції", layout="wide")

# Оновлена функція для вставки iframe з універсальною висотою
def dw_chart(url, height=750):
    components.html(
        f"""
        <div style="padding: 10px;">
            <iframe src="{url}" 
                    scrolling="no" 
                    frameborder="0" 
                    style="border: none; width: 100%; height: {height}px;">
            </iframe>
        </div>
        """,
        height=height + 50
    )

# 2. Головний заголовок
st.markdown("""
    <div style="background-color: #1a2a3a; padding: 25px; border-radius: 10px; text-align: center; margin-bottom: 40px;">
        <h1 style="color: white; margin: 0;">Міграційні тенденції</h1>
        <p style="color: #cccccc; font-size: 1.2rem;">Візуалізація даних про міжнародну міграцію</p>
    </div>
""", unsafe_allow_html=True)

# 3. Блок 1: Світова карта
st.header("Зосередження мігрантів у світі")
col_map1, col_txt1 = st.columns([1.8, 1])

with col_map1:
    dw_chart("https://datawrapper.dwcdn.net/V5G9Z/1/", height=700)

with col_txt1:
    st.write("##") # Відступ зверху
    st.write("""
    Карта демонструє розподіл міжнародних мігрантів за основними регіонами світу. 
    Найбільша концентрація спостерігається в Європі та Азії. Це пов'язано з високим рівнем економічного розвитку, 
    можливостями працевлаштування та стабільною соціальною інфраструктурою.
    """)

st.divider()

# 4. Блок 2: "Рулетка" по регіонах
st.header("Детальний аналіз за регіонами")
regions = ["Європа", "Азія", "Північна Америка", "Південна Америка", "Африка", "Океанія"]
urls = [
    "https://datawrapper.dwcdn.net/yWfM8/1/", # Європа
    "https://datawrapper.dwcdn.net/uPUWn/1/", # Азія
    "https://datawrapper.dwcdn.net/iZseD/1/", # Пн. Америка
    "https://datawrapper.dwcdn.net/0HIfD/1/", # Пд. Америка
    "https://datawrapper.dwcdn.net/5lHVX/1/", # Африка
    "https://datawrapper.dwcdn.net/TdhfV/1/"  # Океанія
]
texts = [
    "Регіон з найвищою концентрацією внутрішньорегіональної міграції. Спільний ринок праці та спрощений перетин кордонів сприяють активному руху населення між країнами ЄС",
    "Азія демонструє найшвидші темпи зростання міграційних потоків. Це зумовлено як економічним стрибком окремих країн, так і великою кількістю трудових мігрантів у регіоні Перської затоки",
    "Традиційний лідер за кількістю іммігрантів. Міграційні процеси тут мають історично сталий характер і значною мірою впливають на демографічну структуру та ринок праці США та Канади",
    "Регіон характеризується значними потоками всередині континенту, що часто спричинені політичною нестабільністю або економічними кризами в окремих країнах",
    "Міграція в Африці часто має вимушений характер через конфлікти або кліматичні зміни, проте спостерігається і позитивна тенденція економічної міграції до великих хабів, як-от ПАР чи Нігерія",
    "Попри невелику загальну кількість людей, регіон має один із найвищих у світі показників мігрантів на душу населення, що забезпечує сталий розвиток економік Австралії та Нової Зеландії"
]

tabs = st.tabs(regions)

for i in range(len(regions)):
    with tabs[i]:
        c1, c2 = st.columns([1.8, 1])
        with c1:
            dw_chart(urls[i], height=750) # Збільшена висота для всіх регіонів
        with c2:
            st.write("##") # Відступ зверху
            st.subheader(f"Особливості регіону: {regions[i]}")
            st.info(texts[i])

st.divider()

# 5. Блок 3: Аналітика та динаміка
st.header("Аналітика та динаміка")

# Графік по роках
st.subheader("Зміна міграції протягом років")
dw_chart("https://datawrapper.dwcdn.net/BmtjW/1/", height=550)
st.write("Ця візуалізація демонструє, як за останні десятиліття частка мігрантів у різних частинах світу перерозподілялася.")

# Графік відношення до населення
st.subheader("Відношення мігрантів до населення")
dw_chart("https://datawrapper.dwcdn.net/pRxv4/2/", height=400)
st.write("Цей графік відображає частку мігрантів у загальній структурі населення кожного регіону.")

# 6. Футер
st.info("Практична робота з візуалізації даних. Виконано за допомогою Python та Datawrapper.")